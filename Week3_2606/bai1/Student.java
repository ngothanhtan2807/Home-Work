package Week3_2606.bai1;

public class Student {
    public String name;
    public int age;
    public int score;

    public Student(String name, int age) {

        this.name = name;
        this.age = age;
        this.score = 10;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
